/*Matrix Rain Effect Scripting*/
// Initialising the canvas
var canvas = document.querySelector('canvas'),
    ctx = canvas.getContext('2d');
    matrixGreen = ctx.fillstyle='#0f0';
    matrixPurple = ctx.fillstyle = '#3E007F';

// Setting the width and height of the canvas
canvas.width = window.innerWidth;
canvas.height = window.innerHeight;

// Setting up the letters

const katakanaMatrixCharacters = 'アァカサタナハマヤャラワガザダバパイィキシチニヒミリヰギジヂビピウゥクスツヌフムユュルグズブヅプエェケセテネヘメレヱゲゼデベペオォコソトノホモヨョロヲゴゾドボポヴッン';
const latinCharacters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
const numsCharacters = '0123456789';
var lettersAlphabet = katakanaMatrixCharacters + latinCharacters + numsCharacters;
lettersAlphabet = lettersAlphabet.split('');

// Setting up the columns
var fontSize = 10,
    columns = canvas.width / fontSize;

// Setting up the drops
var drops = [];
for (var i = 0; i < columns; i++) {
  drops[i] = 1;
}

// Setting up the draw function
function draw() {
  ctx.fillStyle = 'rgba(0, 0, 0, .1)';
  ctx.fillRect(0, 0, canvas.width, canvas.height);
  for (var i = 0; i < drops.length; i++) {
    var text = lettersAlphabet[Math.floor(Math.random() * lettersAlphabet.length)];
    ctx.fillStyle = matrixGreen;
    ctx.fillText(text, i * fontSize, drops[i] * fontSize);
    drops[i]++;
    if (drops[i] * fontSize > canvas.height && Math.random() > .95) {
      drops[i] = 0;
    }
  }
}

// Loop the animation
setInterval(draw, 33);

